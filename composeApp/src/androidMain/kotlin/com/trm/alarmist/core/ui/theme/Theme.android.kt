private fun isContrastAvailable(): Boolean =
  Build.VERSION.SDK_INT >= Build.VERSION_CODES.UPSIDE_DOWN_CAKE

@Composable
private fun selectSchemeForContrast(darkTheme: Boolean): ColorScheme {
  val context = LocalContext.current
  val isPreview = LocalInspectionMode.current
  return when {
    !isPreview && isContrastAvailable() -> {
      val uiModeManager = context.getSystemService(Context.UI_MODE_SERVICE) as UiModeManager
      val contrastLevel = uiModeManager.contrast
      when (contrastLevel) {
        in 0.0f..0.33f -> {
          if (darkTheme) darkScheme else lightScheme
        }
        in 0.34f..0.66f -> {
          if (darkTheme) mediumContrastDarkColorScheme else mediumContrastLightColorScheme
        }
        in 0.67f..1.0f -> {
          if (darkTheme) highContrastDarkColorScheme else highContrastLightColorScheme
        }
        else -> {
          if (darkTheme) darkScheme else lightScheme
        }
      }
    }
    darkTheme -> darkScheme
    else -> lightScheme
  }
}

@Composable
actual fun AppTheme(darkTheme: Boolean, dynamicColor: Boolean, content: @Composable () -> Unit) {
  val colorScheme = selectSchemeForContrast(darkTheme = darkTheme)

  val view = LocalView.current
  if (!view.isInEditMode) {
    SideEffect {
      val window = (view.context as Activity).window
      window.statusBarColor = colorScheme.primary.toArgb()
      WindowCompat.getInsetsController(window, view).isAppearanceLightStatusBars = darkTheme
    }
  }

  MaterialTheme(colorScheme = colorScheme, content = content)
}
